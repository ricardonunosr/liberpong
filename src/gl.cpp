static HMODULE opengl32Dll = GetModuleHandleA("OpenGL32.dll");
static void* win32GetGLFunctionPtr(char *name)
{
	void * addr = GetProcAddress(opengl32Dll,name);
	if(!addr) addr = wglGetProcAddress(name);
	return addr;
}
    
